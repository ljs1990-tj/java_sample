package E_반복문;

import java.util.Scanner;

public class 예시코드_반복문 {
	public static void main(String[] args) {
		/**
		 * == 아래 예시코드는 '스캐너' 내용을 먼저 선행하시고 참고하시기 바랍니다.
		 * [기초 반복문 학습 예제]
		 * 가장 많이 쓰이는 for 문과 while 문의 기본 구조를 공부합니다.
		 * 주석에 적힌 실행 순서를 따라가며 코드의 흐름을 이해해 보세요.
		 */
		
		Scanner sc = new Scanner(System.in);

		// ---------------------------------------------------------
        // 1. for 문: 정해진 횟수만큼 반복하기
        // 실행 순서: ①초기화 -> ②조건식 확인 -> ③코드 실행 -> ④증감식 -> ②조건식 확인...
        // ---------------------------------------------------------
        System.out.println("=== [1. for 문: 1부터 5까지 출력] ===");
        
        for (int i = 1; i <= 5; i++) {
            // i가 1부터 시작해서 5가 될 때까지 1씩 증가하며 반복합니다.
            System.out.println("현재 i의 값: " + i);
        }
        System.out.println();


        // ---------------------------------------------------------
        // 2. while 문: 조건이 참인 동안 계속 반복하기
        // 실행 순서: ①조건식 확인 -> ②코드 실행 -> ③조건식 확인...
        // ---------------------------------------------------------
        System.out.println("=== [2. while 문: 5부터 1까지 카운트다운] ===");
        
        int count = 5; // 초기화
        while (count >= 1) { // 조건식: count가 1 이상인 동안만 실행
            System.out.println("카운트다운: " + count);
            count--; // 증감식: 이 코드가 없으면 무한 루프에 빠집니다!
        }
        System.out.println("발사!");
        System.out.println();


        // ---------------------------------------------------------
        // 3. 반복문을 활용한 합계 계산 (실무 패턴)
        // 1부터 사용자가 입력한 숫자까지의 합을 구합니다.
        // ---------------------------------------------------------
        System.out.println("=== [3. 응용: 1부터 N까지의 합 구하기] ===");
        System.out.print("어디까지 더할까요? 숫자를 입력하세요: ");
        int target = sc.nextInt();
        
        int sum = 0; // 합계를 저장할 변수 (0으로 초기화 필수!)
        
        for (int i = 1; i <= target; i++) {
            sum += i; // sum = sum + i; 와 같습니다. i값을 계속 누적해서 더합니다.
        }
        
        System.out.println("1부터 " + target + "까지의 합계는 " + sum + "입니다.");
        
        // Scanner 자원 반납
        sc.close();
	}
}
